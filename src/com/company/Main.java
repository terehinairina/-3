package com.company;

public class Main {
    public static void main(String[] args) {
        for (int p = 1; p <= 10; p++) {
            for (int o = 1; o <= 10; o++) {
                System.out.println(p + " x " + o + " = " + p * o);
            }
            System.out.println("");
        }
    }
}